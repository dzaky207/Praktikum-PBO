public class Konstanta {
    public static void main(String[]args) {
        //konstanta
        final double PI = 3.14159;
        int radius = 5; 
        double luasLingkaran = PI * radius * radius;
        
        System.out.println("Luas lingkaran dengan radius: " + radius + " adalah " + luasLingkaran);
    }
}
